function get_groups(groups_array){
    groups_array.forEach(function (value, key) {
        $('.tab-type[data-type="group"] .group-list').append('<span class="group-item" data-group="'+ key +'" style="color: '+ value[0] +'; background-color: '+ value[0] +';"></span>');
    });
    $('.tab-type[data-type="group"] .group-list').on('click', '.group-item', function () {
        $('.tab-type[data-type="group"] .group-list .group-item').removeClass('active');
        $(this).addClass('active');
        get_shades($(this).attr('data-group'), groups_array);
        setTimeout(function () {
            $('.tab-type[data-type="group"]').css('display', 'none');
            $('.tab-type[data-type="shade"]').css('display', 'block');
            $('.interier__descriptions').css('opacity', 1);
        }, 500);
    });
}
function get_shades(current_group, groups_array) {
    /* get shades list */
    for (let pair of groups_array) {
        if(pair[0] == current_group){
            pair[1][1].forEach(function (element) {
                $('.tab-type[data-type="shade"] .shade-list').append('<span class="shade-item" data-code="'+ element[1] +'" data-color="'+ element[0] +'" data-price="'+ element[2] +'" style="color: '+ element[0] +'; background-color: '+ element[0] +';"></span>');
            });
        }
    }
    $('.interier__vector').addClass('active'); // show colors on svg
    $('.tab-type[data-type="shade"] .shade-list').on('click', '.shade-item', function () {
        $('.tab-type[data-type="shade"] .shade-list .shade-item').removeClass('active');
        $(this).addClass('active');
        /* get data for info block */
        $('#price-info').css('background-color', $(this).attr('data-color'));
        $('#info-code').html($(this).attr('data-code'));
        $('#info-price').html($(this).attr('data-price'));
        /* show info block / button */
        $('#price-info').slideDown();
        $('#order-btn').slideDown();
    });
}

